DROP TABLE WR_MEMBER;

CREATE TABLE WR_MEMBER(
	WID VARCHAR2(30) NOT NULL PRIMARY KEY,
	WPASS VARCHAR2(30) NOT NULL,
	WNAME VARCHAR2(30) NOT NULL,
	WRESID NUMBER(20) NOT NULL,
	WGENDER VARCHAR2(10) NOT NULL,
	WJOB VARCHAR2(30) NOT NULL,
	WPHONE VARCHAR2(20) NOT NULL,
	WEMAIL VARCHAR2(30) NOT NULL,
	WPOST VARCHAR2(10) NOT NULL,
	WADDRESS VARCHAR2(200) NOT NULL,
	WADDRLAT NUMBER(30, 25) NOT NULL,
	WADDRLNG NUMBER(30, 25) NOT NULL,
	WPAIN VARCHAR2(10) NOT NULL,
	WKEY VARCHAR2(100) NOT NULL,
	WINTRO VARCHAR2(500),
	WDATE DATE NOT NULL,
	WSECRET VARCHAR2(15) NOT NULL
)


